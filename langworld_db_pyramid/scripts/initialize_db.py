import argparse
import sys

from pyramid.paster import bootstrap, setup_logging
from sqlalchemy.exc import OperationalError

from langworld_db_data.langworld_db_data.filetools.csv_xls import read_csv
from langworld_db_data.langworld_db_data.constants.paths import FILE_WITH_DOCULECTS

from .. import models


def setup_models(dbsession):
    """
    Add or update models / fixtures in the database.

    """
    doculect_rows = read_csv(FILE_WITH_DOCULECTS, read_as='dicts')
    for row in doculect_rows:
        row['string_id'] = row['id']
        del row['id']  # ID will be autogenerated integer

        for bool_key in ('is_extinct', 'is_multiple', 'has_feature_profile'):
            row[bool_key] = int(row[bool_key])

        # print(row)
        doculect = models.doculect.Doculect(**row)
        dbsession.add(doculect)


def parse_args(argv):
    parser = argparse.ArgumentParser()
    parser.add_argument(
        'config_uri',
        help='Configuration file, e.g., development.ini',
    )
    return parser.parse_args(argv[1:])


def main(argv=sys.argv):
    args = parse_args(argv)
    setup_logging(args.config_uri)
    env = bootstrap(args.config_uri)

    try:
        with env['request'].tm:
            dbsession = env['request'].dbsession
            setup_models(dbsession)
    except OperationalError:
        print('''
Pyramid is having a problem using your SQL database.  The problem
might be caused by one of the following things:

1.  You may need to initialize your database tables with `alembic`.
    Check your README.txt for description and try to run it.

2.  Your database server may not be running.  Check that the
    database server referred to by the "sqlalchemy.url" setting in
    your "development.ini" file is running.
            ''')
